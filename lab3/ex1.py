# لرسم على صورة سوداء
import numpy as np
import cv2

# إنشاء صورة سوداء فارغة بأبعاد 512x512
img = np.zeros((512, 512, 3), np.uint8)

# رسم خط أحمر قطري
# الإحداثيات (0,0) إلى (511,511)، اللون (0,0,255) -> BGR، السماكة 4
cv2.line(img, (0, 0), (511, 511), (0, 0, 255), 4)

# رسم مستطيل أخضر
# الزاوية العلوية اليسرى (250,50)، الزاوية السفلية اليمنى (400,200)، اللون (0,255,0)، السماكة 2
cv2.rectangle(img, (250, 50), (400, 200), (0, 255, 0), 2)

# رسم دائرة بيضاء
# المركز (100,300)، نصف القطر 80، اللون (255,255,255)، السماكة 2
cv2.circle(img, (100, 300), 80, (255, 255, 255), 2)

# كتابة نص على الصورة
font = cv2.FONT_HERSHEY_SIMPLEX
# النص 'OpenCV'، الموضع (10,450)، الخط، حجم الخط 2، اللون أبيض، السماكة 5
cv2.putText(img, 'OpenCV', (10, 450), font, 2, (255, 255, 255), 5)

# عرض الصورة
cv2.imshow('image', img)
cv2.waitKey(0)
cv2.destroyAllWindows()